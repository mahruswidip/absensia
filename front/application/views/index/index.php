<script type="text/javascript">
    var site_url = '<?php echo site_url() ?>';
    $(function() {
        loadData();
        $('#search_submit').click(function(e) {
            e.preventDefault();
            loadData();
        })
        $('#form-saran').submit(function(e) {
            e.preventDefault();
            $.ajax({
                type: 'ajax',
                method: 'POST',
                url: site_url + 'index/kirimPesan',
                data: $('#form-saran').serialize(),
                dataType: 'json',
                success: function(response) {
                    if (response.success) {
                        Swal.fire('Proses Berhasil!', response.message, 'success').then(function() {
                            $('#form-saran')[0].reset();
                        })
                    } else {
                        console.log(response.message);
                    }
                },
                error: function(xmlresponse) {
                    console.log(xmlresponse);
                }
            })
        })
    })

    function loadData() {
        $.ajax({
            type: 'ajax',
            method: 'POST',
            url: site_url + '/index/loadData',
            dataType: 'json',
            data: $('#form-pencarian').serialize(),
            success: function(response) {
                $('#container-barang').html(response.data);
            },
            error: function(xmlresponse) {
                console.log(xmlresponse)
            }
        })
    }
</script>